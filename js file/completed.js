document.getElementById('task-1').addEventListener('click', function(){
    alert('board updated successfully')
   const totalTask = document.getElementById('total-task').innerText;
   const task = document.getElementById('task').innerText;
   const convertedTotalTask = parseFloat(totalTask);
   const convertedTask = parseFloat(task);
   const sum = convertedTotalTask + 1;
   const minus = convertedTask - 1;
   document.getElementById('total-task').innerText = sum;
   document.getElementById('task').innerText = minus;

   const disableButton1 = document.getElementById('task-1');
    disableButton1.disabled = true;
    disableButton1.style.backgroundColor = 'gray';
   
})
